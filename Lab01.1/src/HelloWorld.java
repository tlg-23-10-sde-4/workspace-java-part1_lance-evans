/*
 * This is the classic hello world application in Java.
 * It is a standalone application(i.e., runs directly on the JVM).
 * It is what we call a "main-class". A class definition with the main() method.
 */
class HelloWorld {
    // Type main for short main method creation
    public static void main(String[] args)
    {
        // Type sout for shortcut to System.out.println
        System.out.println("Hello from IntelliJ!");
        System.out.println("This is the easy part");
        System.out.println("It only gets harder from here...");
    }
}